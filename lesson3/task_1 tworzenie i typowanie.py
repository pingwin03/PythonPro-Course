imie = "Rafał"           # typ str (tekst)
wiek = 42               # typ int (liczba całkowita)  
srednia = 4.75           # typ float (liczba zmiennoprzecinkowa)
student = True           # typ bool (wartość logiczna)

# Wyświetlenie wartości i typów zmiennych
print(f"Imię: {imie}, typ: {type(imie)}")
print(f"Wiek: {wiek}, typ: {type(wiek)}")
print(f"Średnia ocen: {srednia}, typ: {type(srednia)}")
print(f"Student: {student}, typ: {type(student)}")